public class Mang {
   private int x;

    public Mang() {
    }

    public Mang(int x) {
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    @Override
    public String toString() {
        return "SinhSo{" + "x=" + x + '}';
    }
    
}
